package lec02_Variable_type;

public class VariableType {

	// 변수의 타입
	
	// 정수 타입
	byte byteVar = 1;
	short shortvar = 2;
	int intVal = 3;
	long longVal = 4;
	char charVar = 5;
	char charVal = 'a';
	// 한개만 들어가기 때문에 공백이 들어가면 안 됨.
	
	// 실수 타입
	float floatVar = 1.23f;
	// float는 실수 뒤에 f나 F를 붙여 줘야 됨.
	double doubleVar = 1.24;
	
	// 불리언 타입 (true/false)
	boolean boolVar = true;
	
	String strVar = " counting star~    ";
	// String 혼자 대문자, 색도 다름.
	
	
	
	
}
